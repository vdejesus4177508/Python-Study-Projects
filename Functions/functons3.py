def uc(a):
    """
    Converts the input string to uppercase and prints it.
    """
    a = a.upper()  ## Call the upper method with parentheses

    return a      ## Optionally return the uppercase string

## Call the function with your desired string


answer = uc("hello world")  ## This will also return the uppercase string
print("The returned result is:", answer)  ## Print the returned result


