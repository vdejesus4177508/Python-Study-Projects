



# This code demonstrates the use of if and elif statements to check multiple conditions

forms = ['animal', 'vegetable', 'mineral']
answer = input("Are you an animal, vegetable, or mineral? ")

if answer == forms[0]:
    print("You are an animal.GRR!")
elif answer == forms[1]:
    print("You are a mineral. You must be healthy")
elif answer == forms[2]:
    print("You are a vegetable. ROCK ON!")
else:
    print("You did not give a valid response")



